package com.ceiba.parqueadero.modelo;

public class TiempoTotalDeParqueo {

	public long dia;
	public long hora;
	public long minutos;
	
	public long getDia() {
		return dia;
	}
	public void setDia(long dia) {
		this.dia = dia;
	}
	public long getHora() {
		return hora;
	}
	public void setHora(long hora) {
		this.hora = hora;
	}
	public long getMinutos() {
		return minutos;
	}
	public void setMinutos(long minutos) {
		this.minutos = minutos;
	}
}
